/* Reader                                                              */
/* ------------------------------------------------------------------ */

/* Apply a single stanza body to all matching surfaces in `m`. */
static void shader_apply(mc_model_t *m, const char *name, const char *body, size_t body_len) {
	int matched = 0;
	for (int i = 0; i < m->numSurfaces; ++i) {
		if (!strcasecmp(m->surfaces[i].shader, name)) {
			matched = 1;
			break;
		}
		char surf_stem[MC_MAX_PATH];
		mc_q_strncpy(surf_stem, m->surfaces[i].shader, sizeof(surf_stem));
		mc_strip_extension(surf_stem);
		if (!strcasecmp(surf_stem, name)) {
			matched = 1;
			break;
		}
	}
	if (!matched)
		return;

	/* Walk the body collecting flags + every stage's map / blendfunc /
	   alphafunc / tcGen so we can pick the best diffuse stage instead
	   of always grabbing the first one (WoP shaders frequently put
	   animated water or decal effects in stage 0 and the actual base
	   texture in a later stage). */
	const char *p = body;
	const char *end = body + body_len;
	char tok[MC_MAX_PATH];
	int two_sided = -1;
	char surfaceparm[64];
	surfaceparm[0] = 0;
	char deform[32];
	deform[0] = 0;
	char editor_image[MC_MAX_PATH];
	editor_image[0] = 0;
	char normal_map[MC_MAX_PATH];
	normal_map[0] = 0;
	char normal_height_map[MC_MAX_PATH];
	normal_height_map[0] = 0;
	char emissive_map[MC_MAX_PATH];
	emissive_map[0] = 0;

	enum { MAX_STAGES = 16 };
	struct stage_info {
		char map[MC_MAX_PATH];
		int has_blendfunc;	  /* 0 = none (GL_ONE GL_ZERO opaque) */
		int alpha_mode;		  /* -1 unset, 0 opaque, 1 mask, 2 blend */
		float alpha_cutoff;
		int env_mapped;		  /* tcGen environment / tcMod turb */
	} stages[MAX_STAGES];
	int num_stages = 0;
	int in_stage = 0;
	struct stage_info cur;
	memset(&cur, 0, sizeof(cur));
	cur.alpha_mode = -1;
	cur.alpha_cutoff = 0.5f;

	while (p && p < end) {
		const char *next = sh_token(p, tok, sizeof(tok));
		if (!next || next > end)
			break;
		p = next;
		if (!tok[0])
			break;

		if (!strcmp(tok, "{")) {
			in_stage = 1;
			memset(&cur, 0, sizeof(cur));
			cur.alpha_mode = -1;
			cur.alpha_cutoff = 0.5f;
			continue;
		}
		if (!strcmp(tok, "}")) {
			if (in_stage && num_stages < MAX_STAGES)
				stages[num_stages++] = cur;
			in_stage = 0;
			continue;
		}

		char low[MC_MAX_PATH];
		mc_q_strncpy(low, tok, sizeof(low));
		sh_lower(low);

		if (!in_stage) {
			if (!strcmp(low, "cull")) {
				char arg[64];
				p = sh_token(p, arg, sizeof(arg));
				sh_lower(arg);
				if (!strcmp(arg, "disable") || !strcmp(arg, "none") || !strcmp(arg, "twosided"))
					two_sided = 1;
				else
					two_sided = 0;
			} else if (!strcmp(low, "surfaceparm")) {
				p = sh_token(p, surfaceparm, sizeof(surfaceparm));
			} else if (!strcmp(low, "deformvertexes")) {
				char arg[64];
				p = sh_token(p, arg, sizeof(arg));
				sh_lower(arg);
				if (!strcmp(arg, "autosprite") || !strcmp(arg, "autosprite2"))
					mc_q_strncpy(deform, arg, sizeof(deform));
			} else if (!strcmp(low, "qer_editorimage")) {
				if (!editor_image[0])
					p = sh_token(p, editor_image, sizeof(editor_image));
			} else if (!strcmp(low, "normalmap") || !strcmp(low, "bumpmap")) {
				/* Treat _nh-suffixed paths as normal+height. */
				char arg[MC_MAX_PATH];
				p = sh_token(p, arg, sizeof(arg));
				char stem[MC_MAX_PATH];
				mc_q_strncpy(stem, arg, sizeof(stem));
				mc_strip_extension(stem);
				size_t slen = strlen(stem);
				if (slen >= 3 && !strcasecmp(stem + slen - 3, "_nh"))
					mc_q_strncpy(normal_height_map, arg, sizeof(normal_height_map));
				else
					mc_q_strncpy(normal_map, arg, sizeof(normal_map));
			}
			continue;
		}

		/* In a stage. */
		if (!strcmp(low, "map") || !strcmp(low, "clampmap")) {
			char arg[MC_MAX_PATH];
			p = sh_token(p, arg, sizeof(arg));
			if (arg[0] != '$' && !cur.map[0])
				mc_q_strncpy(cur.map, arg, sizeof(cur.map));
		} else if (!strcmp(low, "animmap")) {
			char arg[64];
			p = sh_token(p, arg, sizeof(arg)); /* freq */
			char first[MC_MAX_PATH];
			p = sh_token(p, first, sizeof(first));
			if (!cur.map[0])
				mc_q_strncpy(cur.map, first, sizeof(cur.map));
		} else if (!strcmp(low, "blendfunc")) {
			cur.has_blendfunc = 1;
			char arg[64];
			p = sh_token(p, arg, sizeof(arg));
			sh_lower(arg);
			if (!strcmp(arg, "blend") || !strcmp(arg, "gl_src_alpha"))
				cur.alpha_mode = 2;
			else if (!strcmp(arg, "add"))
				cur.alpha_mode = 2;
			else if (!strcmp(arg, "filter") || !strcmp(arg, "gl_dst_color") ||
					 !strcmp(arg, "gl_zero"))
				/* multiplicative filter stage = decoration on top of base */
				cur.alpha_mode = -1;
			/* skip second arg if present */
			const char *peek = sh_skip(p);
			if (*peek != '{' && *peek != '}' && *peek) {
				char arg2[64];
				p = sh_token(p, arg2, sizeof(arg2));
				sh_lower(arg2);
				/* GL_ONE GL_ZERO is effectively opaque (replaces). */
				if (!strcmp(arg, "gl_one") && !strcmp(arg2, "gl_zero")) {
					cur.has_blendfunc = 0;
					cur.alpha_mode = -1;
				}
			}
		} else if (!strcmp(low, "alphafunc")) {
			char arg[64];
			p = sh_token(p, arg, sizeof(arg));
			sh_lower(arg);
			cur.alpha_mode = 1;
			if (!strcmp(arg, "ge128"))
				cur.alpha_cutoff = 128.0f / 255.0f;
			else if (!strcmp(arg, "gt0"))
				cur.alpha_cutoff = 1.0f / 255.0f;
			else if (!strcmp(arg, "lt128"))
				cur.alpha_cutoff = 128.0f / 255.0f;
		} else if (!strcmp(low, "tcgen")) {
			char arg[64];
			p = sh_token(p, arg, sizeof(arg));
			sh_lower(arg);
			if (!strcmp(arg, "environment"))
				cur.env_mapped = 1;
		}
	}

	/* Pick the diffuse stage:
	   1. prefer a stage whose map basename matches the shader basename
	      (e.g. shader "models/weapons2/betty/betty" -> "betty.tga")
	   2. otherwise prefer the last opaque-ish (no blendfunc) non-env
	      stage with a map (the conventional final base layer)
	   3. otherwise prefer the last non-env stage with a map
	   4. otherwise the first stage with a map
	   5. otherwise qer_editorimage. */
	const char *want_stem = mc_basename(name);
	int chosen = -1;
	for (int i = 0; i < num_stages; ++i) {
		if (!stages[i].map[0] || stages[i].env_mapped)
			continue;
		const char *bn = mc_basename(stages[i].map);
		char stem[MC_MAX_PATH];
		mc_q_strncpy(stem, bn, sizeof(stem));
		mc_strip_extension(stem);
		if (!strcasecmp(stem, want_stem)) {
			chosen = i;
			break;
		}
	}
	if (chosen < 0) {
		for (int i = num_stages - 1; i >= 0; --i) {
			if (stages[i].map[0] && !stages[i].env_mapped && !stages[i].has_blendfunc) {
				chosen = i;
				break;
			}
		}
	}
	if (chosen < 0) {
		for (int i = num_stages - 1; i >= 0; --i) {
			if (stages[i].map[0] && !stages[i].env_mapped) {
				chosen = i;
				break;
			}
		}
	}
	if (chosen < 0) {
		for (int i = 0; i < num_stages; ++i) {
			if (stages[i].map[0]) {
				chosen = i;
				break;
			}
		}
	}

	char map_path[MC_MAX_PATH];
	int alpha_mode = -1;
	float alpha_cutoff = 0.5f;
	if (chosen >= 0) {
		mc_q_strncpy(map_path, stages[chosen].map, sizeof(map_path));
		alpha_mode = stages[chosen].alpha_mode;
		alpha_cutoff = stages[chosen].alpha_cutoff;
		/* If this isn't the first map-bearing stage, any alphaFunc /
		   blend on it is for compositing over the earlier base layers
		   (e.g. WoP weapons paint a decal on top of an animated water
		   stage).  Standalone in a glTF viewer that would mask out the
		   transparent regions of the texture, so treat as opaque. */
		int first_map = -1;
		for (int i = 0; i < num_stages; ++i) {
			if (stages[i].map[0]) { first_map = i; break; }
		}
		if (chosen != first_map)
			alpha_mode = 0;
	} else if (editor_image[0]) {
		mc_q_strncpy(map_path, editor_image, sizeof(map_path));
	} else {
		map_path[0] = 0;
	}

	for (int i = 0; i < m->numSurfaces; ++i) {
		mc_surface_t *s = &m->surfaces[i];
		/* MD3 surface shaders sometimes carry a .tga/.jpg/.png suffix
		   (e.g. "models/.../yellflare.tga") while shader scripts use
		   the extensionless form. Match either way. */
		char surf_stem[MC_MAX_PATH];
		mc_q_strncpy(surf_stem, s->shader, sizeof(surf_stem));
		mc_strip_extension(surf_stem);
		if (strcasecmp(s->shader, name) && strcasecmp(surf_stem, name))
			continue;
		if (map_path[0] && !s->texture[0])
			mc_q_strncpy(s->texture, map_path, sizeof(s->texture));
		if (two_sided >= 0)
			s->two_sided = two_sided;
		if (alpha_mode >= 0) {
			s->alpha_mode = alpha_mode;
			if (alpha_mode == 1)
				s->alpha_cutoff = alpha_cutoff;
		}
		if (surfaceparm[0])
			mc_q_strncpy(s->surfaceparm, surfaceparm, sizeof(s->surfaceparm));
		if (deform[0])
			mc_q_strncpy(s->deform, deform, sizeof(s->deform));
		if (normal_map[0] && !s->normal_map[0])
			mc_q_strncpy(s->normal_map, normal_map, sizeof(s->normal_map));
		if (normal_height_map[0] && !s->normal_height_map[0])
			mc_q_strncpy(s->normal_height_map, normal_height_map, sizeof(s->normal_height_map));
		if (emissive_map[0] && !s->emissive_map[0])
			mc_q_strncpy(s->emissive_map, emissive_map, sizeof(s->emissive_map));
		/* Snapshot the raw stanza body so a glTF round-trip can re-emit
		   the full set of stages verbatim instead of regenerating a
		   stripped-down approximation from the parsed metadata. */
		if (body && body_len > 0) {
			free(s->q3_shader_body);
			s->q3_shader_body = (char *)mc_malloc(body_len + 1);
			if (s->q3_shader_body) {
				memcpy(s->q3_shader_body, body, body_len);
				s->q3_shader_body[body_len] = 0;
			}
		}
	}
}

int mc_load_q3shader(const char *path, mc_model_t *m) {
	size_t sz = 0;
	unsigned char *raw = mc_read_file(path, &sz);
	if (!raw)
		return -1;
	const char *src = (const char *)raw;
	const char *p = src;
	int found = 0;

	for (;;) {
		char name[MC_MAX_QPATH];
		const char *next = sh_token(p, name, sizeof(name));
		if (!next || !name[0])
			break;
		p = next;
		/* Expect a '{' opening the stanza body. */
		const char *peek = sh_skip(p);
		if (*peek != '{')
			continue;
		p = peek + 1;

		/* Walk balanced braces to find the matching close. */
		const char *body = p;
		int depth = 1;
		while (*p && depth > 0) {
			p = sh_skip(p);
			if (*p == '{') {
				++depth;
				++p;
			} else if (*p == '}') {
				--depth;
				if (depth == 0)
					break;
				++p;
			} else {
				char tmp[256];
				const char *n2 = sh_token(p, tmp, sizeof(tmp));
				if (!n2)
					break;
				p = n2;
			}
		}
		if (depth != 0)
			break;
		const char *bodyEnd = p; /* points at the matching '}' */
		shader_apply(m, name, body, (size_t)(bodyEnd - body));
		++found;
		if (*p == '}')
			++p;
	}

	free(raw);
	MC_LOG("parsed %s (%d shader stanza%s)\n", path, found, found == 1 ? "" : "s");
	return 0;
}

