/*
===========================================================================
modelconverter - Quake3 .shader file reader and writer.

The writer turns the materials carried on each mc_surface_t into a Q3
shader script: one stanza per unique shader name, using the surface's
texture as the first stage's `map` and emitting `cull disable`,
`surfaceparm trans`, `blendFunc blend`, normal/emissive comments and so
on according to the recorded glTF material flags.

The reader parses an existing `.shader` file and, for any surface whose
shader name matches a stanza, copies the first stage `map` path into the
surface's texture slot and lifts a few common flags (cull, surfaceparm,
blendFunc, alphaFunc) back onto the material.  This lets a `.shader`
authored by hand drive the textures referenced by an MD3.

The parser is intentionally tiny: it understands nested {}, line and
block comments, "quoted strings" and whitespace-separated tokens.
That is sufficient for everything WoP and ioquake3 ship.
===========================================================================
*/

#include "mc_common.h"

#include <ctype.h>
#include <string.h>

/* ------------------------------------------------------------------ */
/* Tiny string helpers                                                */
/* ------------------------------------------------------------------ */

static int sh_is_token_char(int c) {
	return c && !isspace(c) && c != '{' && c != '}' && c != '"';
}

/* Skip whitespace and // / *-style comments.  Returns the new cursor. */
static const char *sh_skip(const char *p) {
	for (;;) {
		while (*p && isspace((unsigned char)*p))
			++p;
		if (p[0] == '/' && p[1] == '/') {
			while (*p && *p != '\n')
				++p;
			continue;
		}
		if (p[0] == '/' && p[1] == '*') {
			p += 2;
			while (*p && !(p[0] == '*' && p[1] == '/'))
				++p;
			if (*p)
				p += 2;
			continue;
		}
		return p;
	}
}

/* Read the next token into `out` (size `out_size`).  Returns the cursor
   just past the token, or NULL at end-of-input. */
static const char *sh_token(const char *p, char *out, size_t out_size) {
	p = sh_skip(p);
	if (!*p) {
		if (out_size)
			out[0] = 0;
		return NULL;
	}
	size_t i = 0;
	if (*p == '{' || *p == '}') {
		if (out_size > 1) {
			out[0] = *p;
			out[1] = 0;
		}
		return p + 1;
	}
	if (*p == '"') {
		++p;
		while (*p && *p != '"') {
			if (i + 1 < out_size)
				out[i++] = *p;
			++p;
		}
		if (*p == '"')
			++p;
	} else {
		while (sh_is_token_char((unsigned char)*p)) {
			if (i + 1 < out_size)
				out[i++] = *p;
			++p;
		}
	}
	if (out_size)
		out[i] = 0;
	return p;
}

static void sh_lower(char *s) {
	for (; *s; ++s)
		*s = (char)tolower((unsigned char)*s);
}

