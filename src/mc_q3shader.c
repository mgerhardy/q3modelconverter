/* Writer                                                              */
/* ------------------------------------------------------------------ */

/* Strip a trailing extension (.tga/.jpg/.png/...) and any leading "./" so
   the value matches what the engine expects in `map` directives. */
static void shader_clean_path(char *p) {
	if (p[0] == '.' && p[1] == '/')
		memmove(p, p + 2, strlen(p + 2) + 1);
	mc_strip_extension(p);
}

static int shader_already_emitted(const mc_model_t *m, int upto, const char *name) {
	for (int i = 0; i < upto; ++i) {
		if (!strcmp(m->surfaces[i].shader, name))
			return 1;
	}
	return 0;
}

int mc_save_q3shader(const char *path, const mc_model_t *m) {
	FILE *f = fopen(path, "wb");
	if (!f) {
		MC_ERR("error: cannot open '%s' for writing\n", path);
		return -1;
	}

	fprintf(f, "// generated by modelconverter\n");

	int written = 0;
	for (int i = 0; i < m->numSurfaces; ++i) {
		const mc_surface_t *s = &m->surfaces[i];
		if (!s->shader[0])
			continue;
		if (shader_already_emitted(m, i, s->shader))
			continue;

		char tex[MC_MAX_PATH];
		mc_q_strncpy(tex, s->texture[0] ? s->texture : s->shader, sizeof(tex));
		shader_clean_path(tex);

		fprintf(f, "\n%s\n{\n", s->shader);
		/* If we round-tripped the original body through glTF extras,
		   emit it verbatim so every stage / blendFunc / tcMod / rgbGen
		   survives unchanged. Otherwise reconstruct a best-effort
		   stanza from the parsed metadata. */
		if (s->q3_shader_body && s->q3_shader_body[0]) {
			fputs(s->q3_shader_body, f);
			if (s->q3_shader_body[strlen(s->q3_shader_body) - 1] != '\n')
				fputc('\n', f);
			fprintf(f, "}\n");
			++written;
			continue;
		}
		if (s->two_sided)
			fprintf(f, "\tcull disable\n");
		if (s->surfaceparm[0])
			fprintf(f, "\tsurfaceparm %s\n", s->surfaceparm);
		else if (s->alpha_mode == 2)
			fprintf(f, "\tsurfaceparm trans\n");
		/* qer_editorimage helps GtkRadiant pick the texture for previews. */
		if (tex[0])
			fprintf(f, "\tqer_editorimage %s\n", tex);

		/* Optional normal / emissive maps as opengl2-style stages.  The
		   engine ignores them on the renderergl1 path; the comments make
		   it obvious which file fed them. */
		if (s->normal_map[0]) {
			char nm[MC_MAX_PATH];
			mc_q_strncpy(nm, s->normal_map, sizeof(nm));
			shader_clean_path(nm);
			fprintf(f, "\tnormalMap %s\n", nm);
		}
		if (s->normal_height_map[0]) {
			char nh[MC_MAX_PATH];
			mc_q_strncpy(nh, s->normal_height_map, sizeof(nh));
			shader_clean_path(nh);
			/* opengl2 picks up the alpha channel as height when the file
			   ends in _nh; the directive is the same. */
			fprintf(f, "\tnormalMap %s\n", nh);
		}
		if (s->mr_map[0]) {
			char mr[MC_MAX_PATH];
			mc_q_strncpy(mr, s->mr_map, sizeof(mr));
			shader_clean_path(mr);
			fprintf(f, "\tspecularMap %s\n", mr);
		}

		/* Primary stage. */
		fprintf(f, "\t{\n");
		if (tex[0])
			fprintf(f, "\t\tmap %s\n", tex);
		if (s->alpha_mode == 1) {
			int cutoff = (int)(s->alpha_cutoff * 255.0f);
			if (cutoff <= 0)
				cutoff = 128;
			fprintf(f, "\t\talphaFunc GE128\n");
			fprintf(f, "\t\tdepthWrite\n");
			fprintf(f, "\t\t// alphaCutoff %.3f (%d)\n", s->alpha_cutoff, cutoff);
		} else if (s->alpha_mode == 2) {
			fprintf(f, "\t\tblendFunc blend\n");
		}
		fprintf(f, "\t\trgbGen identity\n");
		fprintf(f, "\t}\n");

		/* Optional emissive overlay. */
		if (s->emissive_map[0] ||
			(s->emissive_factor[0] + s->emissive_factor[1] + s->emissive_factor[2]) > 0.0f) {
			char em[MC_MAX_PATH];
			mc_q_strncpy(em, s->emissive_map[0] ? s->emissive_map : tex, sizeof(em));
			shader_clean_path(em);
			fprintf(f, "\t{\n");
			fprintf(f, "\t\tmap %s\n", em);
			fprintf(f, "\t\tblendFunc add\n");
			fprintf(f, "\t\trgbGen const ( %.3f %.3f %.3f )\n", s->emissive_factor[0], s->emissive_factor[1],
					s->emissive_factor[2]);
			fprintf(f, "\t}\n");
		}

		fprintf(f, "}\n");
		++written;
	}

	fclose(f);
	MC_LOG("wrote %s (%d shader stanza%s)\n", path, written, written == 1 ? "" : "s");
	return 0;
}

