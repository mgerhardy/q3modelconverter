/* Parse a .skin file into a list of (surface,shader) entries.  Returns
   the number of entries collected.  Used both by the legacy
   `mc_load_q3skin` (which patches an mc_model_t in place) and by the
   player-bundle loader which keeps every variant verbatim. */
static int parse_q3skin(const unsigned char *raw, size_t sz, mc_skin_entry_t **out) {
	int cap = 0, n = 0;
	mc_skin_entry_t *list = NULL;
	const char *p = (const char *)raw;
	const char *end = p + sz;
	while (p < end) {
		const char *eol = p;
		while (eol < end && *eol != '\n' && *eol != '\r')
			++eol;
		size_t len = (size_t)(eol - p);
		char line[512];
		if (len >= sizeof(line))
			len = sizeof(line) - 1;
		memcpy(line, p, len);
		line[len] = 0;
		p = eol;
		while (p < end && (*p == '\n' || *p == '\r'))
			++p;

		char *hash = strchr(line, '#');
		if (hash)
			*hash = 0;
		char *cmt = strstr(line, "//");
		if (cmt)
			*cmt = 0;

		char *comma = strchr(line, ',');
		if (!comma)
			continue;
		*comma = 0;
		char *sname = line;
		char *texture = comma + 1;
		while (*sname && (*sname == ' ' || *sname == '\t'))
			++sname;
		char *e = sname + strlen(sname);
		while (e > sname && (e[-1] == ' ' || e[-1] == '\t'))
			*--e = 0;
		while (*texture && (*texture == ' ' || *texture == '\t'))
			++texture;
		e = texture + strlen(texture);
		while (e > texture && (e[-1] == ' ' || e[-1] == '\t'))
			*--e = 0;

		if (!sname[0])
			continue;
		if (n == cap) {
			cap = cap ? cap * 2 : 8;
			list = (mc_skin_entry_t *)mc_realloc(list, (size_t)cap * sizeof(mc_skin_entry_t));
		}
		mc_q_strncpy(list[n].surface, sname, sizeof(list[n].surface));
		mc_q_strncpy(list[n].shader, texture, sizeof(list[n].shader));
		++n;
	}
	*out = list;
	return n;
}

int mc_load_q3skin_variant(const char *path, mc_skin_variant_t *out) {
	size_t sz = 0;
	unsigned char *raw = mc_read_file(path, &sz);
	if (!raw)
		return -1;
	free(out->entries);
	out->entries = NULL;
	out->numEntries = parse_q3skin(raw, sz, &out->entries);
	free(raw);
	return 0;
}

int mc_save_q3skin_variant(const char *path, const mc_skin_variant_t *v) {
	FILE *f = fopen(path, "wb");
	if (!f) {
		MC_ERR("error: cannot open '%s' for writing\n", path);
		return -1;
	}
	for (int i = 0; i < v->numEntries; ++i) {
		fprintf(f, "%s,%s\n", v->entries[i].surface, v->entries[i].shader);
	}
	fclose(f);
	return 0;
}

int mc_load_q3skin(const char *path, mc_model_t *m) {
	size_t sz = 0;
	unsigned char *raw = mc_read_file(path, &sz);
	if (!raw)
		return -1;

	mc_skin_entry_t *entries = NULL;
	int n = parse_q3skin(raw, sz, &entries);
	free(raw);
	int hits = 0;
	for (int i = 0; i < n; ++i) {
		const char *sname = entries[i].surface;
		const char *texture = entries[i].shader;
		if (!sname[0] || !texture[0])
			continue;
		if (!strncmp(sname, "tag_", 4))
			continue;
		for (int si = 0; si < m->numSurfaces; ++si) {
			mc_surface_t *s = &m->surfaces[si];
			if (strcasecmp(s->name, sname))
				continue;
			mc_q_strncpy(s->shader, texture, sizeof(s->shader));
			s->texture[0] = 0;
			++hits;
			break;
		}
	}
	free(entries);
	MC_LOG("parsed %s (%d surface override%s)\n", path, hits, hits == 1 ? "" : "s");
	return 0;
}

/* ------------------------------------------------------------------ */
/* Auto-discover .shader files under the asset roots                  */
/* ------------------------------------------------------------------ */


#ifdef _WIN32
#include <windows.h>
#include <sys/stat.h>
#define MC_STAT_STRUCT _stat
#define MC_STAT_FUNC _stat
#else
#include <dirent.h>
#include <sys/stat.h>
#define MC_STAT_STRUCT stat
#define MC_STAT_FUNC stat
#endif

static void scan_dir_for_shaders(const char *dir, const char *const *asset_roots, mc_model_t *m, int depth, int *count);

/* Module-global config so the CLI can tune scan depth and add extra
   --shader-path roots without changing every call site. */
static int g_shader_max_depth = 6;
static char **g_shader_extra_paths = NULL;
static int g_shader_extra_count = 0;

void mc_q3shader_set_max_depth(int depth) {
	if (depth < 0) depth = 0;
	if (depth > 32) depth = 32; /* sanity cap */
	g_shader_max_depth = depth;
}

void mc_q3shader_add_search_path(const char *dir) {
	if (!dir || !dir[0]) return;
	g_shader_extra_paths = (char **)mc_realloc(g_shader_extra_paths,
		sizeof(char *) * (size_t)(g_shader_extra_count + 1));
	g_shader_extra_paths[g_shader_extra_count++] = strdup(dir);
}

static void try_apply_shader_file(const char *path, mc_model_t *m, int *count) {
	/* Quick filter: only files whose name ends in ".shader". */
	size_t l = strlen(path);
	if (l < 7 || strcasecmp(path + l - 7, ".shader"))
		return;
	if (mc_load_q3shader(path, m) == 0)
		++(*count);
}

/* Callback context for PK3 shader scanning. */
typedef struct {
	mc_model_t *m;
	int *count;
} pk3_shader_ctx_t;

static void pk3_shader_cb(const char *pk3, const char *entry, void *user) {
	pk3_shader_ctx_t *ctx = (pk3_shader_ctx_t *)user;
	size_t sz = 0;
	unsigned char *data = mc_pk3_read_entry(pk3, entry, &sz);
	if (!data) return;
	/* mc_load_q3shader expects a file path; write to a temp file. */
	char tmp[MC_MAX_PATH];
	snprintf(tmp, sizeof(tmp), "/tmp/mc_pk3_%s", strrchr(entry, '/') ? strrchr(entry, '/') + 1 : entry);
	if (mc_write_file(tmp, data, sz) == 0) {
		if (mc_load_q3shader(tmp, ctx->m) == 0)
			++(*ctx->count);
		remove(tmp);
	}
	free(data);
}

static void try_apply_pk3(const char *path, mc_model_t *m, int *count) {
	if (!mc_pk3_is_pk3(path)) return;
	pk3_shader_ctx_t ctx = { m, count };
	mc_pk3_for_each(path, ".shader", pk3_shader_cb, &ctx);
}

static void scan_dir_for_shaders(const char *dir, const char *const *asset_roots, mc_model_t *m, int depth, int *count) {
	(void)asset_roots;
	if (depth > g_shader_max_depth)
		return;
#ifdef _WIN32
	char pattern[MC_MAX_PATH];
	snprintf(pattern, sizeof(pattern), "%s\\*", dir);
	WIN32_FIND_DATAA fd;
	HANDLE h = FindFirstFileA(pattern, &fd);
	if (h == INVALID_HANDLE_VALUE)
		return;
	do {
		if (!strcmp(fd.cFileName, ".") || !strcmp(fd.cFileName, ".."))
			continue;
		char child[MC_MAX_PATH];
		snprintf(child, sizeof(child), "%s\\%s", dir, fd.cFileName);
		if (fd.dwFileAttributes & FILE_ATTRIBUTE_DIRECTORY)
			scan_dir_for_shaders(child, asset_roots, m, depth + 1, count);
		else {
			try_apply_shader_file(child, m, count);
			try_apply_pk3(child, m, count);
		}
	} while (FindNextFileA(h, &fd));
	FindClose(h);
#else
	DIR *d = opendir(dir);
	if (!d)
		return;
	struct dirent *de;
	while ((de = readdir(d)) != NULL) {
		if (!strcmp(de->d_name, ".") || !strcmp(de->d_name, ".."))
			continue;
		char child[MC_MAX_PATH];
		snprintf(child, sizeof(child), "%s/%s", dir, de->d_name);
		struct MC_STAT_STRUCT st;
		if (MC_STAT_FUNC(child, &st) != 0)
			continue;
		if (S_ISDIR(st.st_mode))
			scan_dir_for_shaders(child, asset_roots, m, depth + 1, count);
		else if (S_ISREG(st.st_mode)) {
			try_apply_shader_file(child, m, count);
			try_apply_pk3(child, m, count);
		}
	}
	closedir(d);
#endif
}

int mc_autoload_shaders(const char *const *asset_roots, mc_model_t *m) {
	if (!m || m->numSurfaces <= 0)
		return 0;
	int count = 0;
	/* Walk asset_roots first (the canonical Q3 layout: <root>/scripts and
	   <root>/ *.pk3dir/scripts), then any --shader-path the user added.
	   Extra paths are scanned recursively from their root so users can
	   point at "baseq3/" and have us find scripts/ underneath. */
	for (int i = 0; asset_roots && asset_roots[i]; ++i) {
		/* Look for a "scripts" sibling (the standard Q3 layout). */
		char scripts[MC_MAX_PATH];
		snprintf(scripts, sizeof(scripts), "%s/scripts", asset_roots[i]);
		struct MC_STAT_STRUCT st;
		if (MC_STAT_FUNC(scripts, &st) == 0 && S_ISDIR(st.st_mode)) {
			scan_dir_for_shaders(scripts, asset_roots, m, 0, &count);
		}
		/* Also look for *.pk3dir/scripts under this root. */
		char pk3dirs[MC_MAX_PATH];
		mc_q_strncpy(pk3dirs, asset_roots[i], sizeof(pk3dirs));
#ifdef _WIN32
		WIN32_FIND_DATAA fd;
		char pat[MC_MAX_PATH];
		snprintf(pat, sizeof(pat), "%s\\*.pk3dir", pk3dirs);
		HANDLE h = FindFirstFileA(pat, &fd);
		if (h != INVALID_HANDLE_VALUE) {
			do {
				if (!(fd.dwFileAttributes & FILE_ATTRIBUTE_DIRECTORY))
					continue;
				char sub[MC_MAX_PATH];
				snprintf(sub, sizeof(sub), "%s\\%s\\scripts", pk3dirs, fd.cFileName);
				scan_dir_for_shaders(sub, asset_roots, m, 0, &count);
			} while (FindNextFileA(h, &fd));
			FindClose(h);
		}
#else
		DIR *d = opendir(pk3dirs);
		if (d) {
			struct dirent *de;
			while ((de = readdir(d)) != NULL) {
				size_t nl = strlen(de->d_name);
				if (nl < 8 || strcmp(de->d_name + nl - 7, ".pk3dir"))
					continue;
				char sub[MC_MAX_PATH];
				snprintf(sub, sizeof(sub), "%s/%s/scripts", pk3dirs, de->d_name);
				struct MC_STAT_STRUCT sst;
				if (MC_STAT_FUNC(sub, &sst) == 0 && S_ISDIR(sst.st_mode))
					scan_dir_for_shaders(sub, asset_roots, m, 0, &count);
			}
			closedir(d);
		}
#endif
	}
	/* Extra --shader-path roots: scan recursively for any *.shader. */
	for (int i = 0; i < g_shader_extra_count; ++i) {
		struct MC_STAT_STRUCT st;
		if (MC_STAT_FUNC(g_shader_extra_paths[i], &st) == 0 && S_ISDIR(st.st_mode))
			scan_dir_for_shaders(g_shader_extra_paths[i], asset_roots, m, 0, &count);
	}
	if (count)
		MC_LOG("auto-loaded %d shader file%s\n", count, count == 1 ? "" : "s");
	return count;
}
